#include "libft.h"

int	ft_isalnum(int c)
{
	unsigned char a;

	a = c;
	if (a >= 'a' && a <= 'z')
		return (1);
	else if (a >= 'A' && a <= 'Z')
		return (1);
	else if (a >= '0' && a <= '9')
		return (1);
	else
		return (0);
}
